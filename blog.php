                    <h3 class="ui large header"><i class="newspaper outline icon"></i> 5 Best Mobile Apps to Help Students Pick Colleges and Majors</a></h3>
                    <span class="ui text"><i class="user icon"></i> Shelly Palmer</span><br>
                    <span class="ui text"><i class="calendar alternate outline icon"></i> February2, 2015</span><br>
                    <a class="ui text" href="https://www.shellypalmer.com/2015/02/5-best-mobile-apps-to-help-students-pick-colleges-and-majors/"><i class="linkify icon"></i>Link</a><br>
                    <br>
                    <div>
                        <p>High school students hunting for the right university or college have their work cut out for them. There are some – but not many – worthy mobile apps geared toward the college search and application process. These five apps cover choosing a major, virtual college visits, funding, and capturing the social atmosphere of higher learning institutions.</p>
                        <h3>Quad2Quad – College Visit Guru&nbsp;(iOS Free)</h3>
                        <p>Recently updated, the Quad2Quad app focuses exclusively on preparing students and parents for exciting trips to prospective universities. Users can organize the app’s list of 91 fantastic United States colleges alphabetically, by region, or size. Quad2Quad offers pre-planned trips based around basic concepts, like visiting only Ivy League schools or the top schools in the Midwest. Families can plan custom trips as well, a seamless process all-in-all. Particularly handy is the app’s Alerts feature, which details information pertinent to individual institutions, like a school’s criteria for offering interviews and directions to the nearest parking garage on campus or admissions office.</p>
                        <h3>College Passport – SAT Edition&nbsp;(iOS, Free)</h3>
                        <p>Provided by EduPath, College Passport: SAT Edition prepares prospective college students ranging from 9th to 12th grade in many ways. With this app students can explore schools, take short-form SAT tests with instant feedback, and more. The results from practice tests – in writing, math, vocabulary, and reading comprehension and retention – and tuition estimates can either confirm or curb a student’s enthusiasm for their dream school. Each school page displays the number of students, acceptance rate, graduation rate, tuition, an extensive summary about academics and student life, and five similar schools based on student searches. EduPath also offers a version focused around the&nbsp;ACT.</p>
                        <h3>College Majors&nbsp;(iOS, Free)</h3>
                        <p>Are you interested in knowing the salary projection for particular college majors? College Majors offers this information based on levels of education. Citing the U.S. Department of Education, each major includes a description covering how a major can be applied to the real world, annual wages, and new job growth based on forecasted employment increases through 2020. If a student wants to get a Bachelors Degree in Computer Science, she or he simply checks the breakdown of job options based on expected salary. Students can see an average of how long it will take to pay back their student loans/tuition based on individual jobs and the expected job growth. Another neat feature: once students access a job outlook page, they can share a concise infographic with their peers, parents, or advisor.</p>
                        <h3>College FAFSA Finder +&nbsp;(iOS, $2.99)</h3>
                        <p>The unfortunate reality of attending a U.S. college is taking on student loans. FAFSA Finder +, an iOS universal app from Turbo Tuition, provides a searchable database that displays tuition cost, room and board, academic profiles, and over 3,000 colleges. The main goal of FAFSA Finder + is to enable students and parents to pinpoint, as it pertains to higher learning, where quality and affordability overlap. Students can narrow down their options by checking each school’s expectations for ACT and SAT scores as well as GPA requirements. The app also includes college FAFSA and CSS codes, and students can favorite academic institutions that appeal to them.</p>
                        <h3>College Confidential&nbsp;(iOS,&nbsp;Free)</h3>
                        <p>The search for the perfect college can be overwhelming for students. The free College Confidential app, which started off and continues to be a leading website, offers helpful forums for the many questions related to college racing through a high schooler’s mind. Topics include admissions, college life, transferring, studying abroad, pre-college issues, and even graduate school. Thoughtful forum members dole out practical advice and follow-up questions that help direct inquiries to useful information, whether it be links or school rankings.</p>
